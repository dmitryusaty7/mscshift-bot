module.exports = {
  intro: 'Блок 4. Состав бригады. Нажмите «Начать», чтобы заполнить заместителя, водителя и рабочих.',
  startButton: '✍️ Начать заполнение',
  backToShiftMenuButton: '⬅️ В меню смены',
  hubTitle: ({ shipName, date }) =>
    ['<b>Состав бригады</b>', shipName ? `Судно: <b>${shipName}</b>` : null, date ? `Дата: <b>${date}</b>` : null]
      .filter(Boolean)
      .join('\n'),
  hubBody: ({ deputy, driver, workers, ready }) => {
    const list = [
      `${driver ? '✅' : '✍️'} Водитель: <b>${driver || 'не указан'}</b>`,
      `${deputy ? '✅' : '➖'} Заместитель: <b>${deputy || 'не указан'}</b>`,
      `${workers.length ? '✅' : '✍️'} Рабочие: ${workers.length ? '' : '<b>не добавлены</b>'}`,
    ]

    if (workers.length) {
      list.push(...workers.map((w, idx) => `${idx + 1}. ${w}`))
    }

    if (!ready) {
      list.push('\nДля завершения укажите водителя и хотя бы одного рабочего.')
    }

    return list.join('\n')
  },
  deputy: {
    ask: 'Введите ФИО заместителя кириллицей (минимум два слова) или нажмите «Пропустить».',
    skipButton: 'Пропустить',
    saved: (name) => `Заместитель сохранён: ${name}.`,
    cleared: 'Заместитель удалён.',
    driverMissing: 'Нужно сначала добавить водителя, чтобы привязать заместителя.',
  },
  driver: {
    ask: 'Введите ФИО водителя кириллицей (минимум два слова).',
    saved: (name) => `Водитель сохранён: ${name}.`,
    missing: 'Нельзя подтвердить состав без водителя.',
  },
  workers: {
    ask: 'Введите ФИО рабочего кириллицей (минимум два слова).',
    added: (name) => `Рабочий добавлен: ${name}.`,
    removed: (name) => `Рабочий удалён: ${name}.`,
    duplicate: 'Этот рабочий уже добавлен в смену.',
    empty: 'Нельзя подтвердить состав без хотя бы одного рабочего.',
  },
  validationError: 'Используйте только кириллицу и минимум два слова. Пример: «Иванов Иван».',
  hubUpdated: 'Хаб состава обновлён.',
  actionCancelled: 'Возвращаюсь в хаб состава.',
  confirmReady: 'Состав заполнен. Возвращаю в меню смены.',
  returnToMenu: 'Возвращаю в меню смены без подтверждения состава.',
  unexpectedError: 'Не удалось обновить состав. Попробуйте снова.',
}
